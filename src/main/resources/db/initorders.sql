DROP TABLE IF EXISTS Orders CASCADE;
DROP TABLE IF EXISTS Ordersdetails CASCADE;
CREATE TABLE Orders
(
    id integer
       GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    date date,
    dayofweek smallint,
    time time,
    check_value float,
    shipping_info text
)
;

